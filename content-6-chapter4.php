The Secret Trick of SciFi

</p>

<p>

<br>
The concept of the u-, eu- and dystopia is often used in the literature genre of Science fiction (SciFi). SciFi is not always given great credibility [22]. It is often seen as a childish genre of fantastic but unreal tales of aliens and starships, a view primarily based on prejudice and misconception. In fact, Brave New World and 1984 fall under the genre of SciFi, but are not seen as such by critics because they are “too well written to be SciFi”. 
<br>
SciFi can be defined as: “a genre of fantastic tales that seeks plausibility against a background of science”. Eric Rabkin, Professor in English Language and Literature calls Science Fiction, “the most important genre today” for it provides, “the language of the modern world”[23]. 1984’s <span id="bigbrother">Big Brother</span> is nowadays used when talked about privacy, Star Wars became the name of the United States’ <span id="starwars">space</span> defence system. 
<br>
The powerful aspect of science fiction is the combination of both emotional and intellectual excitement. It plays with our senses, offers us bizarre extraordinary inventions give us the feeling of discovering new worlds. But for a good science fiction writer this is just the start. As Frederik Pohl says: “<i>A good science fiction story should be able to predict not the automobile but the traffic jam</i>[3].”. Science fiction makes us think of the implications of technological growth, but gets our attention by offering fantastic high adventure stories. 
<br>
For example, Mary Shelley's novel, <i><span id="frankenstein">Frankenstein</span></i> [24][25][26][27], tells the fantastic story of a crazy professor creating a monster out of loose body parts. A story that could be seen as a bizarre modern fairy tale, but at the same time it raises ethical questions. What would happen if an egocentric scientist used technology irresponsibly? Is it moral to create an individual? How would this be for the creature? 
<br>

<br>
As with utopia, the genre often shows us critique on social structures, or specific for SciFi, science itself. For example, in <i>Gulliver’s Travels</i>, Jonathan Swift tells us about the island of <span id="laputa">laputa</span>. Above this island we find a floating rock inhabited by scientists. The scientists exact tribute from the farmers that are living below them. If the farmers refuse to pay, the scientists manipulate an array of magnets that makes the island float in such a manner that the floating rock threatens to crush the farmers below. The story doesn't only give us an entertaining adventurous tale next to an exploration of magnet technology, but also shows us power structures as we have known them for ages and thus questions the power of science itself.
<br>

<br>
SciFi questions our present and future often using contemporary phenomena as a starting point and reversing or extrapolating them [18]. An example are the novels, <i>Herland</i> by Charlotte Perkins Gilman and <i>The Handmaid’s Tale</i> by Margaret Atwood. Both the stories start from the idea of patriarchy, but both the books criticize it in a different manner. Herland reverses the idea by showing a world led by women [29]. The Handmaid’s Tale extrapolates patriarchy and shows an extreme totalitarian patriarchal society [30].
<br>

<br>
Over the past decades, we’ve seen the border between SciFi and reality dissolve. Flat displays, drones, the internet, artificial intelligence, pocket computers; all technology once proposed in SciFi. This predictive social power, combined with SciFi’s ability to imagine the social consequences of innovation makes it an interesting genre for developers [23]. They use SciFi as a source of inspiration; The first mobile <span id="mobile">phone</span> Motorola made was inspired by a “<i><span id="communicator">communicator</span></i>” from <i>Star Trek</i>. But more importantly, tech-companies hire SciFi writers to speculate on the social, practical and ethical implications of their products. 
<br>
Google, for instance, had a team of writers speculating on what a regular day would be like wearing Google Glasses . The stories are then used to optimize the design. Apple, Microsoft, Oculus, Siemens, and Intel are known to hire or support science fiction writers [24]. 
<br>
When we study SciFi, we recognize some things that were mentioned before. We see its use of the concept of the u- and dystopia. We also see it uses different ways of storytelling, to serve different purposes. storytelling is a very fruitful way to present a fictional world. To get to know how this could be done without a story, we will have to look at other disciplines.
<br>

