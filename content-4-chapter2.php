Historical Speculation

</p>

<p>

Even in the early days of civilization, humans have been speculating about, and imagining their future. This was done in a number of different manners. Sometimes it was a prediction of prophets or soothsayers, later a philosophical essays and works of fiction. These future images didn’t always serve the same purpose. Some were meant to be predictions that were expected to become reality, whereas others served as alternative realities that that were meant to provoke contemplation on the current one. What these worlds had in common was the possibility of their worlds—that weren't real yet—to become reality.
<br>

<br>
The cave painting in <span id="lascaux">Lascaux</span> could be seen as the earliest material clue of an image that depicted the future. According to one of the interpretations, the paintings were produced to enhance the possibility of a prosperous hunt. If the hunters depicted the catching of the animal, their chances of success improved. This shows that these hunters did not only have a notion of a complex idea like “the future”, but they were also aware they could influence it. However, it remains a question if the visualisations were strategic or ritualistic in nature. 
<br>

<br>
The first written futures could be found in the sacred scriptures of the jews [12]. The <span id="torah">jewish</span> people lacked a glorious past to be proud of, but they did have a terrible past that they wanted to forget. Because of this, they relied on the predictions of prophets to boost their collective self-esteem. According to these prophecies, the jews were awaiting a grand future. They had a special place in God's plan. The past was interpreted in retrospect as a series of fulfilled prophecies on the way until God would proclaim His Holy Kingdom. In this case we see that the predicting of the future serves the purpose of giving feelings of hope and pride.
<br>

<br>
Predictions can also have a practical value. The Greek doctor <span id="hippocratus">Hippocrates</span> wrote about this in 400 B.C. Hippocrates called the art of prediction an excellent activity for physicians. When a doctor was able to predict the course of a disease, he would be prepared for what was still coming. There wasn’t enough time to heal every patient. It was more efficient to predict which patient was surely going to die. No energy had to be wasted on hopeless cases. 
<br>

<br>
<span id="plato">Plato</plato>, a contemporary of Hippocrates, used his book <i>The Republic</i> to put forward his design for the perfect society [13]. The Republic isn’t a prediction, but a proposal for an alternative that could exist, and according to Plato, should exist. 
<br>

<br>
Plato’s approach might seem rational, but not all of his contemporaries were like him. In ancient Rome, there was a strong belief that the future could be revealed by all kind of seers[12]. These soothsayers used all kinds of inherited techniques for prediction. Their techniques were once based on empirical observations, but lost their original meaning over time. Previous generations would, for instance, look at the sky and the flight behavior of birds to forecast the weather, or look at the organs of deceased cattle to determine the cause of death. The Roman seers like <i><span id="augur">augur</span></i> or <i>haruspex</i> used similar predictors such as flight behavior and animal organs to predict all kinds of unrelated business. Through these rituals, the seers communicated visions about the will of the gods or predictions on economy.
<br>
The Romans were so dependant on predictions that even the senate consulted a book of predictions. These Sibylline Books were once bought by the “half-legendary” king Tarquinius <span id="superbus">Superbus</span> and were full of Greek hexametric verses. The books were exclusively meant for use by the senate, but the Roman population also wanted to profit from these verses. This led to false copies of the book being sold on the black market. For safety reasons, the senate ordered the burning of 2000 of these copies in 12 B.C.. 
<br>
<span id="lucian">Lucian</span> found this course of affairs so absurd that he wrote the satirical novel: <i>The True History</i>[12][14]. The book dealt with future predictions that were meant to entertain and critique simultaneously. It contains ideas about extraterrestrial life and travels to mars and venus. Two decennia before its invention, it can be seen as the first work of science fiction.
<br>

